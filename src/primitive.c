/*
 * Primitive functions (+ - * / define)
 *
 */

#include "primitive.h"


double p_add (double a, double b) {
    return a + b;
}

double p_sub (double a, double b) {
    return a - b;
}

double p_mul (double a, double b) {
    return a * b;
}

double p_div (double a, double b) {
    return a / b;
}

